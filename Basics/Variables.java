public class Variables {
    public static void main(String[] args) {

        // Basic Variable Declaration
        int age = 25;
        System.out.println("Age: " + age);

        // Multiple Variable Declaration
        int x = 10, y = 20, z = 30;
        System.out.println("X: " + x + ", Y: " + y + ", Z: " + z);

        // Chained Assignment
        int a, b, c;
        a = b = c = 50;
        System.out.println("a=" + a + ", b=" + b + ", c=" + c);

    }
}
